#!/usr/bin/python3

def add(a, b):
    """My addition function

    args:
        a: first integer
        b: second integer

    Returns:
        the return value. a + b
    """

    return (a + b)

def sub(a, b):
    """My subtraction function

    args:
        a: first integer
        b: second integer

    Returns:
        the return value. a - b
    """

    return (a - b)

def mul(a, b):

    """My multiplication function
    args:
        a: first integer
        b: second integer
   
    Returns:
        the return value. a * b
    """
    
    return (a * b)

def div(a, b):

    """My division function
    args:
        a: first integer
        b: second integer
    Returns:
        the return value. a / b
    """
    
    return (a / b)


